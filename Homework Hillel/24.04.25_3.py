def split_list(lst):
    # Якщо список порожній, повертаємо два порожні списки
    if len(lst) == 0:
        return [[], []]

    # Знаходимо індекс для розділення списку
    mid = len(lst) // 2

    # Якщо кількість елементів непарна, перший список буде мати більше елементів
    if len(lst) % 2 != 0:
        mid += 1

    # Розділяємо список на два
    return [lst[:mid], lst[mid:]]

# Приклад 1: Порожній список
lst1 = []
result1 = split_list(lst1)
print(result1)  # Виведе: [[], []]

# Приклад 2: Парна кількість елементів
lst2 = [1, 2, 3, 4]
result2 = split_list(lst2)
print(result2)  # Виведе: [[1, 2], [3, 4]]

# Приклад 3: Непарна кількість елементів
lst3 = [1, 2, 3, 4, 5]
result3 = split_list(lst3)
print(result3)  # Виведе: [[1, 2, 3], [4, 5]]